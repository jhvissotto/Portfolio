-- ============================================ --
-- ================ SELECT ALL ================ --
-- ============================================ --
SELECT * 
FROM Posts

-- ================================================ --
-- ================ SELECT COLUMNS ================ --
-- ================================================ --
SELECT Slug, Title 
FROM Posts

-- ============================================ --
-- ================ SELECT ONE ================ --
-- ============================================ --
SELECT *
FROM Posts
WHERE Slug = 'post-1'

-- =============================================== --
-- ================ SORTED SELECT ================ --
-- =============================================== --
SELECT * 
FROM Posts
ORDER BY Title ASC

-- ================================================ --
-- ================ LIMITED SELECT ================ --
-- ================================================ --
SELECT *
FROM Posts
LIMIT 3

-- ================================================ --
-- ================ SELECT INCLUDE ================ --
-- ================================================ --
SELECT * 
FROM Posts
JOIN Posts_Users ON Posts.Slug          = Posts_Users.fk_post
JOIN Users       ON Posts_Users.fk_user =       Users.Email

-- =========================================== --
-- ================ SELECT BY ================ --
-- =========================================== --
SELECT * 
FROM Posts
JOIN Posts_Users ON Posts.Slug = Posts_Users.fk_post
WHERE fk_user = 'user1@mail.com'
